package com.rays.conditional;

public class MathclassOperation {
public static void main(String[] args) {
 System.out.println("mathematic operation");
 System.out.println("max " + Math.max(2, 5));
 System.out.println("min "+ Math.min(3, 5));
 System.out.println("Absolute 4.7" + Math.abs(4.7));
 System.out.println("exp 10"+ Math.exp(10));
 System.out.println("Random number"+ Math.random());
 System.out.println("square root"+ Math.sqrt(4));
}
}
